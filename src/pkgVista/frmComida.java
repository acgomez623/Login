    private void btnConsultarComidaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnConsultarComidaActionPerformed
        // TODO add your handling code here:
        pkgNegocio.clsDAOComida obj;
        obj=new pkgNegocio.clsDAOComida();
        obj.setCodigo(txtCodigo.getText());

        String resultado=obj.consultarComida(2);
        String resultado2=obj.consultarComida(3);
        if(resultado.equals("no")){
            JOptionPane.showMessageDialog(rootPane, "No esta");
        }
        else{
            txtNombreProducto.setText(resultado);
            txtPrecio.setText(resultado2);
        }
    }//GEN-LAST:event_btnConsultarComidaActionPerformed

    private void btnBorrarComidaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBorrarComidaActionPerformed
        // TODO add your handling code here:
        pkgNegocio.clsDAOComida obj;
        obj=new pkgNegocio.clsDAOComida();

        int fila=tbl_Catalogo.getSelectedRow();
        if (fila != -1){
            String codigo=tbl_Catalogo.getValueAt(fila, 0).toString();
            obj.setCodigo(codigo);
            obj.borrarComida();
            JOptionPane.showMessageDialog(rootPane, "Se eliminÃ³ fila");
            listar();
            
        }
        else{
            JOptionPane.showMessageDialog(rootPane, "Para eliminar deberÃ¡ seleccionar una fila!!!!");
        }
    }//GEN-LAST:event_btnBorrarComidaActionPerformed

